package mycar;

public class Ford {

	public static void main(String[] args) {

		/*
		 * 반복문 for 중괄호 블록 내용을 반복적으로 실행할때 사용 종류:for문,while문,do while문 for문:반복 횟수를 알고 있을때
		 * 주로사용 반복변수 반복종료 반복변수 for( 초기값; 조건식; 증감식) int i=0; 1<10; i++) for문을 이용해서 1부터
		 * 10까지 출력하기
		 */
		for (int i =1; i <10; ++i) {
			System.out.println("반복변수 i 값: " + i);
		}//증감식에 따라 반복변수 값을 변화시킴

	}

}
 