package mycar;

public class Benz {
//  value 라는 정수형 변수선언
//	value에 10대입
//	value에 10을 더해서 result라는 변수에 저장
	public static void main(String[] args) {
		int value; //value라는 정수형 변수 선언
		value=10; //value에 10대입
		int result; //result 변수가 없으니 result 선언 해줘야함
		result = value +10; //value 라는 변수가 가지고 있는 값과 10을 더하고 result라는 변수에 저장
		System.out.println("result 값 :"+result);//result 출력값을 보여줘 
		System.out.println("value 값 :"+value);//value 출력값을 보여줘
		//우변 : value 라는 변수가 가지고 있는 값과 10을 더함.
		//그리고 계산결과를 result에 저장.
		
		
	}	

}
